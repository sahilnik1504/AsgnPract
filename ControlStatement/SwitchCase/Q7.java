import java.util.*;

class Q7{

	public static void main (String []args){
		Scanner sc = new Scanner(System.in);

		String plan = sc.next();

		switch(plan){
		
			case "Platinum":

			System.out.println("For the Platinum, the price is 799");
			break;

			case "Gold":

			System.out.println("For the Gold, the price is 599");
			break;

			case "Silver":

			System.out.println("For the Silver, the price is 399");

			default:

			System.out.println("Invalid Input");

		
		}
	
	
	}

}
