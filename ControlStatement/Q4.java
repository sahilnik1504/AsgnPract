class CharDiff{
	public static void main (String []args){
		
		char ch = '1';

		if(ch>='a' && ch<='z'){
		
			System.out.println(ch + " is a Lowercase");
		
		}else if (ch>='A' && ch<='Z'){
		
			System.out.println(ch + " is a Uppercase");
		
		}else

			System.out.println("invalid alphabet");
			
	
	
	}


}
